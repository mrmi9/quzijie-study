function single(stem, choices, correctIndex, explanation, tags) {
  const labels = ['A', 'B', 'C', 'D'];
  return {
    type: 'single',
    stem,
    options: choices.map((text, index) => ({ id: labels[index], label: labels[index], text })),
    correctOptionIds: [labels[correctIndex]],
    explanation,
    tags
  };
}

