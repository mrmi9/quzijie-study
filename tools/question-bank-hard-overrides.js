const labels = ['A', 'B', 'C', 'D'];

function make(type, stem, choices, correctIndexes, explanation, tags) {
  return {
    type,
    stem,
    options: choices.map((text, index) => ({ id: labels[index], label: labels[index], text })),
    correctOptionIds: correctIndexes.map((index) => labels[index]),
    explanation,
    tags
  };
}

const single = (stem, choices, correctIndex, explanation, tags) =>
  make('single', stem, choices, [correctIndex], explanation, tags);
const multiple = (stem, choices, correctIndexes, explanation, tags) =>
  make('multiple', stem, choices, correctIndexes, explanation, tags);
const judge = (stem, correct, explanation, tags) =>
  make('judge', stem, ['正确', '错误'], [correct ? 0 : 1], explanation, tags);

